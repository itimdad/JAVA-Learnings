
public class Switch_cas {
    public static void main(String[] args) {
        int num = 1100;

        switch (num) {
            case 10:
                System.out.println("case 1");
                break;
            case 200:
                System.out.println("case 2");
                break;
            case 100:
                System.out.println("case 3");
                break;
            case 400:
                System.out.println("case 4");
                break;
            default:
                System.out.println("default");
        }

    }

}
