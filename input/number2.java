public class number2 {
    public static void main(String args[]) {

        for (int i = 200; i >= 100; i--) {
            System.out.print(i + " ");
        }

        System.out.println();
        for (char j = 'z'; j >= 'a'; j--) {
            System.out.print(j + " ");

        }

        System.out.println();
        char name = 'a' + 1;
        System.out.println(name);

    }

}
